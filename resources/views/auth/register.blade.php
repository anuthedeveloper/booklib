<script src="https://code.jquery.com/jquery-3.6.0.min.js"></script>
<script>
$(document).ready(function() {
    $('#registerForm').submit(function(event) {
        event.preventDefault(); // Prevent the default form submit
        const formData = $(this).serialize(); // Serialize form data
        $.ajax({
            url: $(this).attr('action'),
            type: 'POST',
            data: formData,
            beforeSend: function() {
                $('#registerBtn').prop('disabled', true).html('<span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span> Please wait...');
            },
            success: function(response) {
                const token = response.token; // Extract the token from response
                localStorage.setItem('token', token); // Store token in localStorage
                window.location.href = '/'; // Redirect to home
            },
            error: function(xhr, status, error) {
                console.error(xhr.responseJSON.message);
                $('#registerBtn').prop('disabled', false).html('Register');
                alert('Registration failed. Please try again.');
            },
            complete: function() {
                $('#registerBtn').prop('disabled', false).html('Register');
            }
        });
    });
});
</script>
